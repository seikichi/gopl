package main

import "testing"

var tests = []struct{ in, want string }{
	{"", ""},
	{"1", "1"},
	{"12", "12"},
	{"123", "123"},
	{"1234", "1,234"},
	{"123456", "123,456"},
	{"1234567", "1,234,567"},
	{"+123", "+123"},
	{"+123.4", "+123.4"},
	{"-123", "-123"},
	{"-123.4", "-123.4"},
	{"+123.567", "+123.567"},
	{"+123.5678", "+123.5678"},
	{"+123.56789", "+123.56789"},
	{"+1234.567", "+1,234.567"},
	{"+1234.5678", "+1,234.5678"},
	{"+1234.56789", "+1,234.56789"},
}

func TestComma(t *testing.T) {
	for _, tt := range tests {
		if got := comma(tt.in); got != tt.want {
			t.Errorf("comma(%q) = %q; want %q", tt.in, got, tt.want)
		}
	}
}
